#include <iostream.h>
#include <conio.h>

int main(){
	int m=82, n=66; // pendeklarasian variable beserta nilainya
	cout << m << endl; // menampilkan nilai m
	cout << n << endl; // menampilkan nilai n
	cout << m << "+" << n << "=" << m+n << endl; // menampilkan nilai pertambahan
	cout << m << "-" << n << "=" << m-n << endl; // menampilkan nilai pengurangan
	cout << m << "*" << n << "=" << m*n << endl; // menampilkan nilai perkalian
	cout << m << "/" << n << "=" << m/n << endl; // menampilkan nilai pembagian
	cout << m << "%" << n << "=" << m%n << endl; // menampilkan nilai modulus
	cout << "-" << m << "=" << -m << endl; // menampilkan nilai m sebagai nilai negatif
}

